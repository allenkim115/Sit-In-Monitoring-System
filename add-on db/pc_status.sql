    room_number VARCHAR(20) NOT NULL,
    status ENUM('available', 'used', 'maintenance') DEFAULT 'available',
    last_updated TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
